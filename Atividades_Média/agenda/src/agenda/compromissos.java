package agenda;

public class compromissos {

	private String data;
	private String hora;
	private String desc;
	
	public compromissos(String data, String hora, String desc) {
		this.data = data;
		this.hora = hora;
		this.desc = desc;
	}
	
	public String getData() {
		return data;
	}
	
	public String getHora() {
		return data;
	}
	
	public String getDesc() {
		return desc;
	}
}
